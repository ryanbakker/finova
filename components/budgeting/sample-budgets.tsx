// Helper function to get total budget and spent amounts
export const getBudgetTotals = (budgets: Budget[]) => {
  return budgets.reduce(
    (acc, budget) => {
      acc.totalBudget += budget.amount;
      acc.totalSpent += budget.spent;
      return acc;
    },
    { totalBudget: 0, totalSpent: 0 }
  );
};

// Helper function to get category data for charts
export const getCategoryData = (budgets: Budget[]) => {
  return budgets.map((budget) => ({
    name: budget.category,
    value: budget.spent,
    budget: budget.amount,
  }));
};
